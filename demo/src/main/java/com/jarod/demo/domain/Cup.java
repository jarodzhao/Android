package com.jarod.demo.domain;

public class Cup {

    private int ml;
    private int cm;

    public Cup() {
    }

    public int getMl() {
        return ml;
    }

    public void setMl(int ml) {
        this.ml = ml;
    }

    public int getCm() {
        return cm;
    }

    public void setCm(int cm) {
        this.cm = cm;
    }
}
